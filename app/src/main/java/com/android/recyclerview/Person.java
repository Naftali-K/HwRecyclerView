package com.android.recyclerview;

public class Person {
    String name;
    String familyName;
    String numberPhone;
    int age;
    char sex;

    public Person() {
    }

    public Person(String name, String familyName, String numberPhone, int age, char sex) {
        this.name = name;
        this.familyName = familyName;
        this.numberPhone = numberPhone;
        this.age = age;
        this.sex = sex;
    }


}
